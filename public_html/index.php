		<p>As the human race continues to grow, within 50 years, the Earth’s population will be at the point of making her burst at the seams. Some population experts put the number in the range of 10 billion, while 50 years after that, it’ll be somewhere in the region of 20 billion. With a population of that size, our world will likely not be able to support itself. Colonizing the red planet is an option with high benefits. Mars’ ice caps should be able to support any life, and, on top of that, there’s plenty of space for potential habitats and settlements. The only downside to this idea is that water would need to be piped to the habitats themselves, as the ice caps themselves wouldn’t provide a strong enough foundation for housing. Overall, however, the ability to colonize the red planet could prove a literal lifesaver for a large portion of the population on Earth.</p>
		<p>With those aside, there are several other reasons for exploration, such as a higher mutation level and fuel sources. In this website, we want you to learn as much as you can about Mars, what it is, why it’s important, and our plans to get there. So look around, enjoy, and best of all, learn. As Buzz Aldrin once said, “Did the Pilgrims on the Mayflower sit around Plymouth Rock waiting for a return trip? They came here to settle. And that's what we should be doing on Mars.” Since we’re in this for the long run, it’s important that we prepare ourselves for the day so that we might also journey up into the stars and to the red planet.</p>
