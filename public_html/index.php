		<p>The possibilities for scientific research are numerous. A probe or rover can only do a certain amount of experiments, and will eventually break down due to dust storms, equipment malfunction, or just general wear and tear. In short, they only have a limited time span and limited capability. A human on Mars would be able to read the instructions given to him/her, perform an almost unlimited amount of actions and projects, and, naturally, a human will likely last far longer than any robot we have available at this time. In addition, the effects of the Martian atmosphere and gravity will have interesting effects on any astronaut, thereby allowing any future missions to plan for the problems facing potential colonists. There are downsides to this, most notably that astronauts would need a food-and-waste disposal system, for both nutrients, fecal waste, and CO2/Oxygen. Overall, however, the ability to have a human presence on Mars will signal a huge step forward in our scientific knowledge of the other planets in our solar system.</p>
